# Ground truth map for validation logic
GROUND_TRUTH = {
    "access_restriction": TABLE_2_ACCESS_RESTRICTION,
    "data_deduplication": TABLE_3_DATA_DEDUPLICATION,
    "encryption_decryption": TABLE_4_ENCRYPTION_DECRYPTION,
    "time_complexity": TABLE_5_TIME_COMPLEXITY
}

def validate_results(computed_results: Dict[str, List[Any]], table_name: str) -> bool:
    """Checks if experimental results match published values exactly.
    
    Args:
        computed_results: The results generated by the current simulation.
        table_name: Key in GROUND_TRUTH dictionary.
        
    Returns:
        bool: True if exact match, False otherwise.
    """
    if table_name not in GROUND_TRUTH:
        logger.error(f"Validation Error: Table '{table_name}' not found in ground truth.")
        return False
        
    ground_truth = GROUND_TRUTH[table_name]
    is_valid = True
    
    for key, expected_values in ground_truth.items():
        if key not in computed_results:
            logger.warning(f"Validation Mismatch [{table_name}]: Key '{key}' missing from computed results.")
            is_valid = False
            continue
            
        actual_values = computed_results[key]
        if actual_values != expected_values:
            logger.warning(
                f"Validation Mismatch [{table_name}] for {key}: "
                f"Expected {expected_values}, Got {actual_values}"
            )
            is_valid = False
        else:
            logger.success(f"Validation Passed [{table_name}] for {key}.")
            
    return is_valid
